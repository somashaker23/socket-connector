const decodeTable = [
  0, 132, 396, 924, 1980, 4092, 8316, 16764,
];

export function linearToUlaw(sample) {
  let sign = (sample >> 8) & 0x80;
  if (sign !== 0) sample = -sample;
  if (sample > CLIP) sample = CLIP;
  sample += BIAS;
  const exponent = encodeTable[(sample >> 7) & 0xff];
  const mantissa = (sample >> (exponent + 3)) & 0x0f;
  return ~(sign | (exponent << 4) | mantissa) & 0xff;
}

export function ulawToLinear(ulaw) {
  ulaw = ~ulaw & 0xff;
  const sign = ulaw & 0x80;
  const exponent = (ulaw >> 4) & 0x07;
  const mantissa = ulaw & 0x0f;
  let sample = decodeTable[exponent] + (mantissa << (exponent + 3));
  if (sign !== 0) sample = -sample;
  return sample;
}

// Float32Array ([-1, 1]) → Uint8Array of μ-law bytes
export function encodeFloat32ToUlaw(float32Array) {
  const result = new Uint8Array(float32Array.length);
  for (let i = 0; i < float32Array.length; i++) {
    const s = Math.max(-1, Math.min(1, float32Array[i]));
    const pcm16 = s < 0 ? s * 0x8000 : s * 0x7fff;
    result[i] = linearToUlaw(Math.round(pcm16));
  }
  return result;
}

// Uint8Array of μ-law bytes → Float32Array ([-1, 1])
export function decodeUlawToFloat32(ulawArray) {
  const result = new Float32Array(ulawArray.length);
  for (let i = 0; i < ulawArray.length; i++) {
    const pcm16 = ulawToLinear(ulawArray[i]);
    result[i] = pcm16 / 0x8000;
  }
  return result;
}
